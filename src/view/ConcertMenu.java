    // Menampilkan data tiket ke dalam jTableConcert
    private void displayTicketData() {
        try {
            conn = (Connection) db.mycon();
            String sql = "SELECT * FROM tiket";
            stm = conn.createStatement();
            rs = stm.executeQuery(sql);

            // Mengisi model dengan data dari result set
            model = new DefaultTableModel(new Object[][]{}, new String[]{"Ticket ID", "Class", "Price"});
            while (rs.next()) {
                model.addRow(new Object[]{
                        rs.getInt("ticket_id"),
                        rs.getString("class"),
                        rs.getDouble("price")
                });
            }

            // Menetapkan model ke jTableConcert
            jTableConcert.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    // Menyimpan data ke dalam tabel tiket
    private void saveTicketData(String ticketClass, double ticketPrice) {
        try {
            conn = (Connection) db.mycon();
            String sql = "INSERT INTO tiket (class, price) VALUES (?, ?)";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, ticketClass);
            pstmt.setDouble(2, ticketPrice);

            // Mengeksekusi pernyataan
            pstmt.executeUpdate();

            // Menampilkan kembali data terbaru ke dalam jTableConcert
            displayTicketData();

            // Membersihkan field setelah penyimpanan
            jTextFieldClass.setText("");
            jTextFieldPrice.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

