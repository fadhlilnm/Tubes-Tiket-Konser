    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:
        // Mendapatkan baris yang dipilih
        int selectedRow = jTableConcert.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data tiket yang akan diubah.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            // Mendapatkan nilai dari kolom yang dipilih
            String ticketClass = jTableConcert.getValueAt(selectedRow, 1).toString();
            double ticketPrice = Double.parseDouble(jTableConcert.getValueAt(selectedRow, 2).toString());

            // Membuat instance dari JDialogEditTicket
            JDialogEditTicket editTicketDialog = new JDialogEditTicket(this, true, ticketClass, ticketPrice);
            editTicketDialog.setLocationRelativeTo(this);
            editTicketDialog.setVisible(true);

            // Menangani hasil dari JDialogEditTicket
            if (editTicketDialog.isDataEdited()) {
                // Mendapatkan data yang telah diubah
                String editedTicketClass = editTicketDialog.getTicketClass();
                double editedTicketPrice = editTicketDialog.getTicketPrice();

                // Melakukan update data di tabel dan database
                updateTicketData(selectedRow, editedTicketClass, editedTicketPrice);
            }
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusActionPerformed
        // TODO add your handling code here:
        // Mendapatkan baris yang dipilih
        int selectedRow = jTableConcert.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data tiket yang akan dihapus.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            // Konfirmasi penghapusan data
            int option = JOptionPane.showConfirmDialog(this, "Anda yakin ingin menghapus data tiket ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                // Menghapus data dari tabel dan database
                deleteTicketData(selectedRow);
            }
        }
    }//GEN-LAST:event_jButtonHapusActionPerformed

    
    
    private void deleteTicketData(int row) {
    try {
        conn = (Connection) db.mycon();
        String deleteSql = "DELETE FROM tiket WHERE ticket_id=?";
        PreparedStatement deletePstmt = conn.prepareStatement(deleteSql);
        deletePstmt.setInt(1, Integer.parseInt(jTableConcert.getValueAt(row, 0).toString()));
        deletePstmt.executeUpdate();

        // Menghapus data dari tabel
        model.removeRow(row);

        JOptionPane.showMessageDialog(this, "Data tiket berhasil dihapus.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
    
    
    private void updateTicketData(int row, String editedTicketClass, double editedTicketPrice) {
    try {
        conn = (Connection) db.mycon();
        String updateSql = "UPDATE tiket SET class=?, price=? WHERE ticket_id=?";
        PreparedStatement updatePstmt = conn.prepareStatement(updateSql);
        updatePstmt.setString(1, editedTicketClass);
        updatePstmt.setDouble(2, editedTicketPrice);
        updatePstmt.setInt(3, Integer.parseInt(jTableConcert.getValueAt(row, 0).toString()));
        updatePstmt.executeUpdate();

        // Mengupdate data di tabel
        jTableConcert.setValueAt(editedTicketClass, row, 1);
        jTableConcert.setValueAt(editedTicketPrice, row, 2);

        JOptionPane.showMessageDialog(this, "Data tiket berhasil diubah.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
    
