    // Metode untuk mengupdate textfield total harga berdasarkan harga tiket dan total tiket
    private void updateTotalPrice() {
        try {
            // Mendapatkan nilai dari textfield harga tiket
            double hargaTiket = Double.parseDouble(jTextFieldPrice.getText());

            // Mendapatkan nilai dari textfield total tiket
            int totalTiket = Integer.parseInt(jTextFieldTotalTicket.getText());

            // Menghitung total harga
            double totalHarga = hargaTiket * totalTiket;

            // Menetapkan nilai pada textfield total harga
            jTextFieldTotalPrice.setText(String.valueOf(totalHarga));
        } catch (NumberFormatException ex) {
            // Tangani jika input tidak valid
            jTextFieldTotalPrice.setText("");
        }
    }
    
    
    private void attachEventListeners() {
        // Menambahkan event listener pada textfield total tiket
        jTextFieldTotalTicket.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                updateTotalPrice();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                updateTotalPrice();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                updateTotalPrice();
            }
        });
    }

    
