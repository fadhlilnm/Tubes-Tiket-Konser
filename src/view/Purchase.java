    private void savePurchaseData() {
        try {
            // Mendapatkan koneksi ke database
            conn = db.mycon();

            // Query SQL untuk menyimpan data ke dalam tabel pembelian
            String sql = "INSERT INTO pesanan (nama_pembeli, no_telp, email, nama_artist, tanggal_konser, lokasi_konser, class_tiket, harga_tiket, jumlah_tiket, total_harga, waktu_pembelian) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

            
            // Membuat prepared statement
            pstmt = conn.prepareStatement(sql);

            // Mengatur nilai parameter pada prepared statement
            pstmt.setString(1, jTextFieldName.getText());
            pstmt.setString(2, jTextFieldPhone.getText());
            pstmt.setString(3, jTextFieldEmail.getText());
            pstmt.setString(4, jTextFieldArtistName.getText());
            pstmt.setString(5, jTextFieldConcertDate.getText());
            pstmt.setString(6, jTextFieldLocation.getText());
            pstmt.setString(7, jTextFieldClassTicket.getText());
            pstmt.setDouble(8, Double.parseDouble(jTextFieldPrice.getText()));
            pstmt.setInt(9, Integer.parseInt(jTextFieldTotalTicket.getText()));
            pstmt.setDouble(10, Double.parseDouble(jTextFieldTotalPrice.getText()));
            pstmt.setString(11, jTextFieldBuyDate.getText());

            // Mengeksekusi query untuk menyimpan data
            pstmt.executeUpdate();

            // Menampilkan pesan sukses
            JOptionPane.showMessageDialog(this, "Data pembelian berhasil disimpan ke dalam database");

            // Menutup prepared statement dan koneksi
            pstmt.close();
        } catch (Exception e) {
            // Menampilkan pesan error jika terjadi kesalahan
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
    
    
